# input() : funcion que permite recibir datos por teclado. Finaliza cuando el usuario presiona la tecla 'Enter'
# print() : funcion que permite mostrar datos por consola
# int() : funcion que permite convertir un string a numeros.

numero1 = int(input("Ingrese un numero: "))
numero2 = int(input("Ingrese otro numero: "))

# Operadores matematicos para numeros
print("Suma", numero1 + numero2)
print("Resta", numero1 - numero2)
print("Multiplicacion", numero1 * numero2)
print("Potencia", numero1 ** numero2)
print("Division", numero1 / numero2)
print("Division Entera", numero1 // numero2)
print("Modulo (resto de la division)", numero1 % numero2)
